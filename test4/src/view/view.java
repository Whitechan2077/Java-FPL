    private void btnOppenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOppenActionPerformed
        try {
            int op = JOptionPane.showConfirmDialog(this,"Do you want to load File\n(Everything on table will be overridden by file data)");
            if(op == JOptionPane.YES_OPTION){
                sv.read("data.dat");
                JOptionPane.showMessageDialog(this, "Open completed");
            fillToTable();
            }
        } catch (IOException ex) {
            Logger.getLogger(view.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Open failed");

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(view.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Open failed");
            JOptionPane.showMessageDialog(this, "Save Completed");

        }
    }//GEN-LAST:event_btnOppenActionPerformed

