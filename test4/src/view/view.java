    public void load() {
        sv.getListCustom().add(new Custom("Nguyen Thuy hang", "Female", 18));
        sv.getListCustom().add(new Custom("Nguyen Anh Dung", "Male", 20));
        sv.getListCustom().add(new Custom("Vu Van Nguyen", "Male", 21));
        sv.getListCustom().add(new Custom("Tran Tuan Phong", "Male", 21));
        fillToTable();
        loadToForm(2);
    }

    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblCustoms.getModel();
        model.setRowCount(0);
        int i = 1;
        for (Custom x : sv.getListCustom()) {
            model.addRow(new Object[]{i++, x.getName(), x.getAge(), x.getSex()});
        }
    }

    public void loadToForm(int index) {
        txtName.setText(sv.getListCustom().get(index).getName());
        txtAge.setText(String.valueOf(sv.getListCustom().get(index).getAge()));
        if (sv.getListCustom().get(index).getSex().equalsIgnoreCase("Male")) {
            rdoMale.setSelected(true);
        } else {
            rdoFemale.setSelected(true);
        }
    }

    public void add() {
        if (rdoMale.isSelected()) {
            sv.add(new Custom(txtName.getText(), String.valueOf(rdoMale.getText()), Integer.parseInt(txtAge.getText())));

        } else {
            sv.add(new Custom(txtName.getText(), String.valueOf(rdoFemale.getText()), Integer.parseInt(txtAge.getText())));
        }
        JOptionPane.showMessageDialog(this, "Add completed");
    }

