    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) tblBitch.getModel();
             model.removeRow(tblBitch.getSelectedRow());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No Data","Dit me may", 2);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

