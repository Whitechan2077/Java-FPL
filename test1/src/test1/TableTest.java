    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblBitch.getModel();
        String[] rows ={txtName.getText(),txtDoB.getText(),txtAddress.getText()};
        model.addRow(rows);
        txtAddress.setText("");
        txtDoB.setText("");
        txtName.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

