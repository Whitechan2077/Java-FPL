    public void showDetail(){
        txtID.setText(svPhone.getList().get(tblPhone.getSelectedRow()).getId());
        txtPrice.setText(String.valueOf(svPhone.getList().get(tblPhone.getSelectedRow()).getPrice()));
        txtRom.setText(String.valueOf(svPhone.getList().get(tblPhone.getSelectedRow()).getRom()));
        cboBrand.setSelectedItem(svPhone.getList().get(tblPhone.getSelectedRow()).getBrand());
        if(svPhone.getList().get(tblPhone.getSelectedRow()).getColor().equalsIgnoreCase("Black")){
            rdoBlack.setSelected(true);
        }
        if(svPhone.getList().get(tblPhone.getSelectedRow()).getColor().equalsIgnoreCase("White")){
            rdoWhite.setSelected(true);
        }
        if(svPhone.getList().get(tblPhone.getSelectedRow()).getColor().equalsIgnoreCase("yellow")){
            rdoYellow.setSelected(true);
        }        
    }
