    public void insert(){
          if (rdoBlack.isSelected()) {
            svPhone.insert(txtID.getText(),String.valueOf(cboBrand.getSelectedItem()),rdoBlack.getText(),Integer.parseInt(txtRom.getText()),Integer.parseInt(txtPrice.getText()));
           }
          if (rdoWhite.isSelected()) {
            svPhone.insert(txtID.getText(),String.valueOf(cboBrand.getSelectedItem()),rdoWhite.getText(),Integer.parseInt(txtRom.getText()),Integer.parseInt(txtPrice.getText()));
           }
          if (rdoYellow.isSelected()) {
            svPhone.insert(txtID.getText(),String.valueOf(cboBrand.getSelectedItem()),rdoYellow.getText(),Integer.parseInt(txtRom.getText()),Integer.parseInt(txtPrice.getText()));
           }
    }
