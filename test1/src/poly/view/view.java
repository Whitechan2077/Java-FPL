    public void  fillTotable(){
        DefaultTableModel model = (DefaultTableModel) tblPhone.getModel();
        model.setRowCount(0);
        int i =1;
        for(Phone x : svPhone.getList()){
            model.addRow(new Object [] {i++,x.getId(),x.getBrand(),x.getRom(),x.getColor(),x.getPrice()});
        }
    }
